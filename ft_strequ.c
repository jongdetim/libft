int	ft_strequ(char const *s1, char const *s2)
{
	int	i;

	if (s1 != s2)
	{
		i = 0;
		while (s1[i] || s2[i])
		{
			if (s2[i] == '\0' || s1[i] == '\0' || s1[i] - s2[i] != 0)
				return (0);
			else
				i++;
		}
	}
	return (1);
}
